import math


print("Решаем уравнение 2x - 5*ln(x) - 3 = 0")
print("Метод Ньютона (касательных)")
print("Точность: 0.000001")

# Просим ввести начальное приближение
x = float(input("Введите начальное приближение x (от 0.5 до 1): "))

# Точность
eps = 0.000001

# Счётчик итераций
n = 0

# Печатаем заголовок таблицы
print("\nИтерации:")
print("№   x           f(x)")

# Основной цикл
while True:
    # Считаем f(x) = 2x - 5*ln(x) - 3
    fx = 2 * x - 5 * math.log(x) - 3

    # Выводим на экран
    print(f"{n}   {x:.8f}   {fx:.8f}")

    # Проверяем, достаточно ли мала f(x)
    if abs(fx) < eps:
        print(f"\nНайден корень: {x:.8f}")
        print(f"f(x) = {fx:.10f}")
        print(f"Итераций: {n}")
        break

    # Считаем производную f'(x) = 2 - 5/x
    dfx = 2 - 5 / x

    # Если производная почти ноль — стоп
    if abs(dfx) < 0.0000001:
        print("Ошибка: производная слишком мала")
        break

    # Формула Ньютона: x_new = x - f(x)/f'(x)
    x = x - fx / dfx

    # Увеличиваем счётчик
    n = n + 1

    # Если много итераций — выходим
    if n > 50:
        print("Слишком много итераций, возможно, нет решения")
        break

print("\nГотово!")